GFX_light::GFX_light(int16_t w, int16_t h) : 
  _width(w), _height(h), _cursorX(0), _cursorY(0), 
  _textSize(1), _textColor(WHITE), _textBgColor(BLACK),
  _textWrap(true), WIDTH(w), HEIGHT(h), rotation(0) {
}

void GFX_light::drawFastVLine(int16_t x, int16_t y, int16_t h, uint16_t color) {
  fillRect(x, y, 1, h, color);
}

void GFX_light::drawFastHLine(int16_t x, int16_t y, int16_t w, uint16_t color) {
  fillRect(x, y, w, 1, color);
}

void GFX_light::fillScreen(uint16_t color) {
  fillRect(0, 0, WIDTH, HEIGHT, color);
}

void GFX_light::setCursor(int16_t x, int16_t y) {
  _cursorX = x; _cursorY = y;
}

void GFX_light::setTextSize(uint8_t s) { _textSize = (s > 0) ? s : 1; }
void GFX_light::setTextColor(uint16_t c) { _textColor = _textBgColor = c; }
void GFX_light::setTextColor(uint16_t c, uint16_t b) { _textColor = c; _textBgColor = b; }
void GFX_light::setTextWrap(boolean w) { _textWrap = w; }

int16_t GFX_light::getCursorX() const { return _cursorX; }
int16_t GFX_light::getCursorY() const { return _cursorY; }
int16_t GFX_light::width(void) const { return _width; }
int16_t GFX_light::height(void) const { return _height; }

void GFX_light::setRotation(uint8_t x) {
  rotation = (x & 3);
  switch (rotation) {
  case 0:
  case 2:
    _width = WIDTH;
    _height = HEIGHT;
    break;
  case 1:
  case 3:
    _width = HEIGHT;
    _height = WIDTH;
    break;
  }
}

void GFX_light::invertDisplay(boolean i) {
  (void)i;
}

size_t GFX_light::write(uint8_t c) {
  if (c == '\n') {
    _cursorX = 0;
    _cursorY += _textSize * FONT_HEIGHT;
    return 1;
  }
  if (c == '\r') {
    return 1;
  }
  if (c < 32) return 0;
  
  drawChar(_cursorX, _cursorY, c, _textColor, _textBgColor, _textSize);
    _cursorX += _textSize * (FONT_WIDTH + CHAR_SPACING);
  if (_textWrap && (_cursorX + _textSize * (FONT_WIDTH + CHAR_SPACING)) > WIDTH) {
    _cursorX = 0;
    _cursorY += _textSize * FONT_HEIGHT;
  }
  return 1;
}

void GFX_light::charBounds(char c, int16_t x, int16_t y, int16_t *w, int16_t *h) {
  (void)c; (void)x; (void)y;
  *w = (FONT_WIDTH + CHAR_SPACING) * _textSize;
  *h = FONT_HEIGHT * _textSize;
}

void GFX_light::drawChar(int16_t x, int16_t y, unsigned char c, uint16_t color, 
                   uint16_t bg, uint8_t size) {
  int16_t charFullWidth = (FONT_WIDTH + CHAR_SPACING) * size;
  int16_t charHeight = FONT_HEIGHT * size;
  
  if(x >= WIDTH || y >= HEIGHT || 
     (x + charFullWidth - 1) < 0 || 
     (y + charHeight - 1) < 0) {
    return;
  }
    uint16_t index = (c - 32) * FONT_WIDTH;
  for(int8_t col = 0; col < FONT_WIDTH; col++) {
    uint8_t line = pgm_read_byte(&font[index + col]);
        uint8_t original_line = line;
        for(int8_t row = 0; row < FONT_HEIGHT; row++) {
      if(original_line & 0x01) {
        if(size == 1) {
          drawPixel(x + col, y + row, color);
        } else {
          fillRect(x + col * size, y + row * size, size, size, color);
        }
      } else if(bg != color) {
        if(size == 1) {
          drawPixel(x + col, y + row, bg);
        } else {
          fillRect(x + col * size, y + row * size, size, size, bg);
        }
      }
      original_line >>= 1;
    }
  }
  
  if(bg != color) {
    for(int8_t col = FONT_WIDTH; col < FONT_WIDTH + CHAR_SPACING; col++) {
      for(int8_t row = 0; row < FONT_HEIGHT; row++) {
        if(size == 1) {
          drawPixel(x + col, y + row, bg);
        } else {
          fillRect(x + col * size, y + row * size, size, size, bg);
        }
      }
    }
  }
}